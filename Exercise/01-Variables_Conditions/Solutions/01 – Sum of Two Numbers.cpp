#include <iostream>
using namespace std;

int main() {
    // Input two numbers
    int num1, num2;
    cout << "Enter first number: ";
    cin >> num1;
    cout << "Enter second number: ";
    cin >> num2;

    // Calculate sum
    int sum = num1 + num2;

    // Output result
    cout << "Sum: " << sum << endl;

    return 0;
}
